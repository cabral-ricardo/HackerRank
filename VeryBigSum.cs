using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace HackerRank
{
    public static class VeryBigSum
    {
        /// <summary>
        /// Sum all values from array
        /// </summary>
        /// <param name="ar"></param>
        /// <returns></returns>
        public static long GetVeryBigSum(long[] ar)
        {
            return ar.Sum(); 
        }
    }
}
